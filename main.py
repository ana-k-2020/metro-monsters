import file_operations
from faker import Faker
import random


def npc():
    name = fake.name().split()
    town = fake.city()
    job = fake.job()

    char_skills = random.sample(skills, 3)
    runic_skills = []
    for skill in char_skills:
        for letter in skill:
            for normal, runic in letters_mapping.items():
                if letter == normal:
                    skill = skill.replace(letter, runic)
        runic_skills.append(skill)

    global context
    context = {
        'first_name': name[0],
        'last_name': name[2],
        'town': town,
        'job': job,
        'strength': random.randrange(3, 19),
        'agility': random.randrange(3, 19),
        'endurance': random.randrange(3, 19),
        'intelligence': random.randrange(3, 19),
        'luck': random.randrange(3, 19),
        'skill_1': runic_skills[0],
        'skill_2': runic_skills[1],
        'skill_3': runic_skills[2]
    }


